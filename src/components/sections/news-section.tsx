export function NewsSection() {
  const { ref, isVisible } = useReveal(0.2)
  const [filter, setFilter] = useState<"all" | "news" | "article">("all")
  const [openId, setOpenId] = useState<number | null>(null)

  const filtered = filter === "all" ? news : news.filter((n) => n.type === filter)

  return (
    <section
      ref={ref}
      data-scrollable
      className="flex h-screen w-screen shrink-0 snap-start items-start px-6 pt-20 pb-6 md:px-12 lg:px-16 overflow-y-auto"
      style={{ scrollbarWidth: "none" }}
    >
      <div className="mx-auto w-full max-w-7xl">
        <div className={`mb-6 transition-all duration-700 ${isVisible ? "translate-x-0 opacity-100" : "-translate-x-12 opacity-0"}`}>
          <h2 className="mb-1 font-sans text-4xl font-light tracking-tight text-foreground md:text-6xl">
            Новости
          </h2>
          <p className="font-mono text-sm text-foreground/60">/ Космические экспедиции и статьи</p>
        </div>

        <div className={`mb-6 flex items-center gap-3 transition-all duration-700 delay-100 ${isVisible ? "opacity-100 translate-y-0" : "opacity-0 translate-y-4"}`}>
          {([
            { key: "all", label: "Все" },
            { key: "news", label: "Новости" },
            { key: "article", label: "Статьи" },
          ] as const).map(({ key, label }) => (
            <button
              key={key}
              onClick={() => setFilter(key)}
              className={`rounded-full border px-4 py-1.5 font-mono text-xs transition-all ${
                filter === key
                  ? "border-blue-500 bg-blue-500/20 text-blue-300"
                  : "border-foreground/20 text-foreground/50 hover:border-foreground/40"
              }`}
            >
              {label}
            </button>
          ))}
          <div className="ml-auto flex items-center gap-1.5 rounded-full border border-foreground/10 bg-foreground/5 px-3 py-1.5">
            <div className="h-1.5 w-1.5 rounded-full bg-green-400 animate-pulse" />
            <span className="font-mono text-[10px] text-foreground/50">Обновлено сегодня</span>
          </div>
        </div>

        <div className="grid gap-4 md:grid-cols-2 lg:grid-cols-3 pb-4">
          {filtered.map((item, i) => (
            <div
              key={item.id}
              className={`rounded-xl border bg-foreground/5 p-4 backdrop-blur-sm transition-all duration-500 ${item.bg} ${isVisible ? "opacity-100 translate-y-0" : "opacity-0 translate-y-8"}`}
              style={{ transitionDelay: `${150 + i * 80}ms` }}
            >
              <div className="mb-3 flex items-start justify-between gap-2">
                <div className="flex items-center gap-2">
                  <div className={`flex h-8 w-8 items-center justify-center rounded-lg bg-foreground/10 ${item.color}`}>
                    <Icon name={item.icon as "Rocket"} fallback="Circle" size={14} />
                  </div>
                  <div>
                    <span className={`font-mono text-[10px] font-medium ${item.color}`}>{item.tag}</span>
                    <div className="font-mono text-[9px] text-foreground/40">{item.date}</div>
                  </div>
                </div>
                <div className="flex items-center gap-1 rounded-full border border-foreground/10 px-2 py-0.5 shrink-0">
                  <Icon name="Clock" size={9} className="text-foreground/40" />
                  <span className="font-mono text-[9px] text-foreground/40">{item.readTime}</span>
                </div>
              </div>

              <h3 className="mb-2 font-sans text-sm font-medium leading-snug text-foreground">{item.title}</h3>

              {openId === item.id ? (
                <div className="mb-3 space-y-2">
                  {item.full.split("\n\n").map((para, pi) => (
                    <p key={pi} className={`font-sans text-xs leading-relaxed ${
                      para.startsWith("**") ? "font-medium text-foreground/90" : "text-foreground/65"
                    }`}>
                      {para.replace(/\*\*/g, "")}
                    </p>
                  ))}
                </div>
              ) : (
                <p className="mb-3 font-sans text-xs leading-relaxed text-foreground/50 line-clamp-2">{item.desc}</p>
              )}

              <button
                onClick={() => setOpenId(openId === item.id ? null : item.id)}
                className={`flex items-center gap-1 font-mono text-[10px] transition-colors ${item.color} opacity-80 hover:opacity-100`}
              >
                <Icon name={openId === item.id ? "ChevronUp" : "ChevronDown"} size={10} />
                {openId === item.id ? "Свернуть" : "Читать полностью"}
              </button>
            </div>
          ))}
        </div>
      </div>
    </section>
  )
}
